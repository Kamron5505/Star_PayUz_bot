def replace_emoji_with_premium(text):
    """Заменить обычные эмодзи на премиум эмодзи с тегами"""
    for emoji, (emoji_id, premium_emoji) in emoji_map.items():
        # Заменяем эмодзи на премиум версию
        pattern = re.escape(emoji)
        replacement = f'<tg-emoji emoji-id="{emoji_id}">{premium_emoji}</tg-emoji>'
        text = re.sub(pattern, replacement, text)
    return text

